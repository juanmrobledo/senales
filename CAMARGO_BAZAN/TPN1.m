% --- Executes on button press in pushbutton2.
function pushbutton2_Callback(hObject, eventdata, handles)

%Seteo de Frecuencia inferior
f1 = get(handles.edit1,'String');
f1 = str2num(f1)

%Seteo de Frecuencia superior
f2 = get(handles.edit2,'String');
f2 = str2num(f2)


%Seteo de duracion de Sine Sweep
Duracion = get(handles.edit3,'String');
Duracion = str2num(Duracion)

%Seteo de frecuencia de muestreo
FrecMuestreo = get(handles.popupmenu3,'String')
FrecMuestreo = str2num(FrecMuestreo)%Carga el sample rate

%Seteo de repeticiones de reproduccion de Sine Sweep
ListaTiempos = get(handles.popupmenu2,'String');
PosTiempoSelec = get(handles.popupmenu2,'value');
Repeticiones = ListaTiempos(PosTiempoSelec);
Repeticiones = str2num(Repeticiones{1})

%Seteo de tiempo de espera entre repeticiones 
TEspera = get(handles.edit4,'String');
TEspera = str2num(TEspera)

%Seteo de posicion de microfono 
PosMic = get(handles.edit5,'String');

%Seteo de Placa de audio
Listaplacas = get(handles.popupmenu1,'String');
PosPlaca = get(handles.popupmenu1,'value');
Placa = Listaplacas(PosPlaca);

%Seteo de Tiempo de Inicio
TiempoTemporizador = get(handles.edit6,'String');
TiempoTemporizador = str2num(TiempoTemporizador)
if (f1>0) & (f2>0) & (Duracion>0) & (FrecMuestreo>0) &  (Repeticiones>0) & (TEspera>0)

if isempty(FrecMuestreo) == 0

if f2 > 20000
    msgbox('Frecuencia maxima debe ser inferior a 20.000 Hz')
else
    try
        Temporizador(TiempoTemporizador); %Inicio la funcion de reproduccion de sonido para salir del recinto
        SineSweepYGrabador(f1,f2,Duracion,FrecMuestreo,Repeticiones,TEspera,PosMic,Placa) % Generacion del sine sweep, reproducion, grabacion y almacenamiento de los datos.
    catch
        msgbox('Valores de entrada incorrectos, verificar')
    end

    
end
else
    msgbox('Actualize la lista de dispositivos para ver las placas actuales')

end
else
        msgbox('Valores de entrada erroneos o negativos, verificar')
end





